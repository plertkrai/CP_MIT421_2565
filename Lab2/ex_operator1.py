# Operators
x = 10
y = 20
z = 30

print("x+y+z = ", x+y+z)

# using f string
print(f"x+y+z = {x+y+z}")
print(f"x-y-z = {x-y-z}") # 10-20-30
print(f"x*y*z = {x*y*z}")
print(f"x/y/z = {x/y/z}")

print(f' x%y = {x%y}')
print(f' x**y = {x**y}')
print(f' x/y = {x/y}')
print(f' x//y = {x//y}')

rs = x/y # 10/20
print(rs,type(rs))

rs = x//y # 10//20
print(rs,type(rs))




